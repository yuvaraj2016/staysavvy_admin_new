        }else{
           
            return redirect()->back()->with('error',$response->json()['message']);
        }

    }

    /**
     * Remove the specified resource from storage.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function destroy($id)
    {
        $session = session()->get('token');

        $response=Http::withToken($session)->withHeaders(['Accept'=>'application/vnd.api.v1+json','Content-Type'=>'application/json'])->delete(config('global.url').'api/confPolicy/'.$id);

        if($response->status()==204){
            // roomtype.index
             return redirect()->route('config_policies.index')->with('success','Config Policy Deleted Sucessfully !..');
        }
        else{


             return redirect()->route('config_policies.index')->with('error',$response->json()['message']);
        }

    }
