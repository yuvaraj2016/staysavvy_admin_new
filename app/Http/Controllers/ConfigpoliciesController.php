    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $session = session()->get('token');


        $response = Http::withToken($session)->withHeaders(['Accept'=>'application/vnd.api.v1+json','Content-Type'=>'application/json'])->post(config('global.url').'api/confPolicy',

        [

            "name"=>$request->name,
          

            "status_id"=>$request->status_id,
           
        ]);


        if($response->status()===201){

            return redirect()->route('config_policies.create')->with('success','Config Policy Created Successfully!');
        }else{

            $request->flash();

            return redirect()->route('config_policies.create')->with('error',$response['errors']);
        }
    }

